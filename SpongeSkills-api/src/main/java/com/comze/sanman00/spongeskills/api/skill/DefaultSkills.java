package com.comze.sanman00.spongeskills.api.skill;

/**
 * This class contains the skills that are supported by this plugin, by default.
 * 
 * <p>Implementations may support other skills which may or may not be exposed 
 * through their own choosing. Further, implementations may not support all, if 
 * any, of the skills that are specified in this class but they must at least provide 
 * a value for each of the fields.</p>
 */
public final class DefaultSkills {
    private DefaultSkills() {

    }

    /**
     * Skill that represents the breaking of stone-like blocks with a pickaxe.
     */
    public static final Skill MINING = null;
    /**
     * Skill that represents the breaking of wood-like blocks with an axe. 
     */
    public static final Skill WOOD_CUTTING = null;
    /**
     * Skill that represents the brewing of potions in a brewing stand.
     */
    public static final Skill ALCHEMY = null;
    /**
     * Skill that represents the repairing of tools in an anvil or in a crafting 
     * table/the inventory.
     */
    public static final Skill REPAIRING = null;
    /**
     * Skill that represents the usage of a sword to kill mobs.
     */
    public static final Skill SWORD_COMBAT = null;
    /**
     * Skill that represents the smelting of ores in a furnace.
     */
    public static final Skill SMELTING = null;
    /**
     * Skill that represents the taming of animals.
     */
    public static final Skill TAMING = null;
    /**
     * Skill that represents the usage of an axe to kill mobs.
     */
    public static final Skill AXE_COMBAT = null;
    /**
     * Skill that represents the usage of the hands, with any item, if any, except 
     * a sword or axe, to kill mobs.
     */
    public static final Skill UNARMED_COMBAT = null;
    /**
     * Skill that represents the harvesting of crops.
     */
    public static final Skill FARMING = null;
    /**
     * Skill that represents the usage of a bow to kill mobs.
     */
    public static final Skill ARCHERY = null;
    /**
     * Skill that represents the catching of fish with a fishing rod.
     */
    public static final Skill FISHING = null;
}
